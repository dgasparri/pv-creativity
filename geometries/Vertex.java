public class Vertex{

    private double pointX;
    private double pointY;
    private double pointZ;

    public Vertex(double x, double y, double z){
        this.pointX = x;
        this.pointY = y;
        this.pointZ = z;
    }

    public double getPointX(){
        return pointX;
    }

    public double getPointY(){
        return pointY;
    }
    
    public double getPointZ(){
        return pointZ;
    }
}
